    "    vec3 worldPos = (uModel * vec4(aPosition, 1.0)).xyz;\n"
    "    vec3 N = normalize(uNormalMatrix * aNormal);\n"
    "    vec3 lit = uAmbient;\n"
    "    for (int i = 0; i < uLightCount && i < 8; ++i) {\n"
    "        vec3 L;\n"
    "        float atten = 1.0;\n"
    "        if (uLights[i].type == 0) { L = -normalize(uLights[i].direction); }\n"
    "        else { vec3 toLight = uLights[i].position - worldPos;\n"
    "            float d = length(toLight);\n"
    "            L = toLight / max(d, 0.0001);\n"
    "            if (uLights[i].range > 0.0) { float r = d / uLights[i].range; atten = max(1.0 - r * r, 0.0); atten *= "
    "atten; }\n"
    "            else atten = 1.0 / (d * d + 0.0001);\n"
    "            if (uLights[i].type == 2) {\n"
    "                float cosA = dot(-L, normalize(uLights[i].direction));\n"
    "                float eps = uLights[i].innerCutoff - uLights[i].outerCutoff;\n"
    "                atten *= (abs(eps) < 0.0001) ? float(cosA >= uLights[i].outerCutoff)\n"
    "                                             : clamp((cosA - uLights[i].outerCutoff) / eps, 0.0, 1.0);\n"
    "            } }\n"
    "        float NdotL = max(dot(N, L), 0.0);\n"
    "        lit += uLights[i].color * uLights[i].intensity * NdotL * atten;\n"
    "    }\n"
    "    vColor = vec4(min(lit, 1.0) * aColor.rgb, aColor.a);\n"
    "    float dist = length(uCameraPos - worldPos);\n"
    "    vFogFactor = 0.0;\n"
    "    if (uFogMode == 1) vFogFactor = clamp((dist - uFogStart) / (uFogEnd - uFogStart), 0.0, 1.0);\n"
    "    else if (uFogMode == 2) vFogFactor = 1.0 - exp(-uFogDensity * dist);\n"
    "    else if (uFogMode == 3) { float d = uFogDensity * dist; vFogFactor = 1.0 - exp(-d * d); }\n"
    "}\n";

static const char *sdl3d_shader_dos_frag =
    "noperspective in vec2 vTexCoord;\n"
    "flat in vec4 vColor;\n"
    "in float vFogFactor;\n"
    "uniform sampler2D uTexture;\n"
    "uniform int uHasTexture;\n"
    "uniform vec4 uTint;\n"
    "uniform vec3 uFogColor;\n"
    "out vec4 fragColor;\n"
    "void main() {\n"
    "    vec2 uv = vec2(vTexCoord.x, 1.0 - vTexCoord.y);\n"
    "    vec4 texel = (uHasTexture != 0) ? texture(uTexture, uv) : vec4(1.0);\n"
    "    vec3 color = texel.rgb * vColor.rgb * uTint.rgb;\n"
    "    float alpha = texel.a * vColor.a * uTint.a;\n"
    "    if (alpha <= 0.0) discard;\n"
    "    color = pow(clamp(color, 0.0, 1.0), vec3(1.0/2.2));\n"
    "    color = mix(color, uFogColor, vFogFactor);\n"
    "    /* DOS VGA: 6-level per channel (216 colors) + heavy Bayer dither. */\n"
    "    float bayer[16] = float[16](0.0/16.0, 8.0/16.0, 2.0/16.0, 10.0/16.0,\n"
    "                                12.0/16.0, 4.0/16.0, 14.0/16.0, 6.0/16.0,\n"
    "                                3.0/16.0, 11.0/16.0, 1.0/16.0, 9.0/16.0,\n"
    "                                15.0/16.0, 7.0/16.0, 13.0/16.0, 5.0/16.0);\n"
    "    ivec2 px = ivec2(gl_FragCoord.xy);\n"
    "    float dither = (bayer[(px.y % 4) * 4 + (px.x % 4)] - 0.5) / 5.0;\n"
    "    color = floor((color + dither) * 6.0 + 0.5) / 6.0;\n"
    "    /* Desaturate toward VGA palette feel. */\n"
    "    float lum = dot(color, vec3(0.299, 0.587, 0.114));\n"
    "    color = mix(vec3(lum), color, 0.7);\n"
    "    /* Slight warm CRT tint. */\n"
    "    color *= vec3(1.05, 0.98, 0.9);\n"
    "    fragColor = vec4(max(color, 0.0), alpha);\n"
    "}\n";

