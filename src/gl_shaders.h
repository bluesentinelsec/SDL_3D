static const char *sdl3d_shader_snes_vert =
    "layout(location = 0) in vec3 aPosition;\n"
    "layout(location = 1) in vec3 aNormal;\n"
    "layout(location = 2) in vec2 aTexCoord;\n"
    "layout(location = 3) in vec4 aColor;\n"
    "uniform mat4 uMVP;\n"
    "uniform mat4 uModel;\n"
    "uniform mat3 uNormalMatrix;\n"
    "uniform vec3 uCameraPos;\n"
    "uniform vec3 uAmbient;\n"
    "uniform int uLightCount;\n"
    "struct Light { int type; vec3 position; vec3 direction; vec3 color;\n"
    "    float intensity; float range; float innerCutoff; float outerCutoff; };\n"
    "uniform Light uLights[8];\n"
    "uniform int uFogMode;\n"
    "uniform float uFogStart;\n"
    "uniform float uFogEnd;\n"
    "uniform float uFogDensity;\n"
    "noperspective out vec2 vTexCoord;\n"
    "flat out vec4 vColor;\n"
    "out float vFogFactor;\n"
    "void main() {\n"
    "    gl_Position = uMVP * vec4(aPosition, 1.0);\n"
    "    vTexCoord = aTexCoord;\n"
    "    vec3 worldPos = (uModel * vec4(aPosition, 1.0)).xyz;\n"
    "    vec3 N = normalize(uNormalMatrix * aNormal);\n"
    "    vec3 lit = uAmbient;\n"
    "    for (int i = 0; i < uLightCount && i < 8; ++i) {\n"
    "        vec3 L;\n"
    "        float atten = 1.0;\n"
    "        if (uLights[i].type == 0) { L = -normalize(uLights[i].direction); }\n"
    "        else { vec3 toLight = uLights[i].position - worldPos;\n"
    "            float d = length(toLight);\n"
    "            L = toLight / max(d, 0.0001);\n"
    "            if (uLights[i].range > 0.0) { float r = d / uLights[i].range; atten = max(1.0 - r * r, 0.0); atten *= "
    "atten; }\n"
    "            else atten = 1.0 / (d * d + 0.0001);\n"
    "            if (uLights[i].type == 2) {\n"
    "                float cosA = dot(-L, normalize(uLights[i].direction));\n"
    "                float eps = uLights[i].innerCutoff - uLights[i].outerCutoff;\n"
    "                atten *= (abs(eps) < 0.0001) ? float(cosA >= uLights[i].outerCutoff)\n"
    "                                             : clamp((cosA - uLights[i].outerCutoff) / eps, 0.0, 1.0);\n"
    "            } }\n"
    "        float NdotL = max(dot(N, L), 0.0);\n"
    "        lit += uLights[i].color * uLights[i].intensity * NdotL * atten;\n"
    "    }\n"
    "    vColor = vec4(min(lit, 1.0) * aColor.rgb, aColor.a);\n"
    "    float dist = length(uCameraPos - worldPos);\n"
    "    vFogFactor = 0.0;\n"
    "    if (uFogMode == 1) vFogFactor = clamp((dist - uFogStart) / (uFogEnd - uFogStart), 0.0, 1.0);\n"
    "    else if (uFogMode == 2) vFogFactor = 1.0 - exp(-uFogDensity * dist);\n"
    "    else if (uFogMode == 3) { float d = uFogDensity * dist; vFogFactor = 1.0 - exp(-d * d); }\n"
    "}\n";

